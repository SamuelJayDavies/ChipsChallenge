package ChipsChallenge;

/**
 * The ItemType enum represents the different types of items
 * in the game CaveQuest.
 * Each enum constant corresponds to a specific type of item.
 * Possible values include KEY, CHIP, NOTHING.
 *
 * @author Samuel Davies
 */
public enum ItemType {

    /**
     * Represents a key item.
     */
    KEY,
    /**
     * Represents a computer chip item.
     */
    CHIP,
    /**
     * Represents the absence of an item.
     */
    NOTHING
}
