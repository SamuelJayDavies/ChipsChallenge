package ChipsChallenge;

/**
 * Colours corresponding to doors and keys. This type is used for equality checking against doors and keys.
 * If the colours match, they are compatible.
 *
 * @author Samuel Davies
 */
public enum Colour {

    /**
     * The colour red.
     */
    RED,
    /**
     * The colour blue.
     */
    BLUE,
    /**
     * The colour green.
     */
    GREEN,
    /**
     * The colour yellow.
     */
    YELLOW
}
